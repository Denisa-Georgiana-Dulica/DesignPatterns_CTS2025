package ctsComportamentale.chain.classesRestaurant;

public class Masa {
    private boolean esteCuratata = false;
    private boolean areServetele = false;
    private boolean areTacamuri = false;

    public boolean isEsteCuratata() {
        return esteCuratata;
    }

    public void setEsteCuratata(boolean esteCuratata) {
        this.esteCuratata = esteCuratata;
    }

    public boolean isAreServetele() {
        return areServetele;
    }

    public void setAreServetele(boolean areServetele) {
        this.areServetele = areServetele;
    }

    public boolean isAreTacamuri() {
        return areTacamuri;
    }

    public void setAreTacamuri(boolean areTacamuri) {
        this.areTacamuri = areTacamuri;
    }
}
